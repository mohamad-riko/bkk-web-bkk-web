                  Sekolah ini merupakan lembaga pendidikan kejuruan yang bertugas untuk meningkatkan sumber daya manusia (SDM) yang terampil, cekatan dan berwawasan dalam pengelolaan sumber daya alam (SDA). Mengingat potensi sumber daya alam yang ada di Kabupaten Indramayu ini salah satunya merupakan daerah penghasil minyak bumi. Oleh karena itu pada awal perdiriannya sekolah ini hanya membuka 1 (satu) program keahlian yaitu Geologi Pertambangan. Meskipun hanya membuka 1 (satu) program keahlian akan tetapi keberadaan SMK Nasional ini di Kabupaten Indramayu disambut baik oleh masyarakat. Hal ini dapat dilihat dari jumlah siswa baru yang mendaftar pada tahun pertama mencapai kurang lebih 350 siswa pendaftar, sedangkan jumlah kelas yang disediakan hanya 6 kelas untuk menampung sekitar 240 siswa. Mengingat animo masyarakat yang demikian besar dan mempertimbangkan potensi daerah yang dimiliki oleh Kabupaten Indramayu sebagai daerah pengolah minyak bumi yaitu dengan berdiri Unit Pengolahan VI Pertamina - Balongan maka pada tahun kedua SMK Nasional membuka 3 (tiga) program keahlian baru, sehingga seluruhnya menjadi 4 program keahlian yaitu Geologi Pertambangan, Mesin Otomotif, Kimia Industri, Akuntansi, Pada tahun ini, siswa baru yang terdaftar dan diterima menjadi siswa di SMK Nasional ini sebanyak 300 siswa untuk mengisi 8 kelas yang disediakan, Menginjak tahun selanjutnya program keahlian kami menjadi 3 (tiga) Geologi Pertambangan, Mesin Otomotif, Kimia Industri . Oleh karena semakin majunya dunia teknologi dan informasi maka pada tahun ajaran 2007/ 2008 Program Keahlian Kimia Industri di tutup dan sekaligus dibukanya Program Keahlian Teknik Informatika Dengan adanya kepercayaan masyarakat yang cukup besar ini, meskipun dengan keterbatasan sarana dan prasarana belajar yang ada, penyelenggara dan pengelola sekolah bahu membahu untuk terus memprogramkan pengembangan sekolah ini untuk meningkatkan mutu pendidikan di SMK Nasional Indramayu.
                </p>

